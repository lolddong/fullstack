package ch01.sec09; // 생략불가, src 아래 하위 폴더를 지정, 맨 윗줄에 위치

public class Hello { // java 파일명과 동일해야함
    public static void main(String[] args) {
        // main 함수인데, 함수가 여러 개일 때 main에서 무조건 시작하게 되어있음
        System.out.println("Hello, World"); // ; 생략 불가능
        // System: 컴퓨터
        // .: 접근 연산자
        // out: 출력장치, 모니터, 스캐너, etc. (output 약자)
        // println("문자열"): ln = line 프린트하고 라인을 넣는 것 (enter)
    }
}
